function w = anglesize(W, s, f)
% Функція розрахунку кутових координат пікселів (проекцій на осі)

% w - кутова величина активної частини пікселя або всього піксеся
% W - Wxi, Wyi - масив даних центрів пікселів
% s - vd, wd, Vd, Wd - розмір активної частини пікселя або всього пікселя
% f - фокусна відстань

w = acot((f ./ (s .* (cos(W) .^ 2))) - (s ./ (4 .* f)));

end
