function dw = diffangle(W, w, s, f)
% Функція розрахунку різниці кутових координат пікселів

% dw - різниця кутових координат пікселів
% w - кутова величина активної частини пікселя або всього піксеся
% W - Wxi, Wyi - масив даних центрів пікселів
% s - vd, wd, Vd, Wd - розмір активної частини пікселя або всього пікселя
% f - фокусна відстань

dw = sign(W) .* acos(((s ./ (2 .* f)) .* (cos(W) .^ 2)...
    .* sin(w) + cos(w)));

end
